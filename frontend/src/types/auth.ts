export interface User {
    id: string;
    email: string;
    name: string;
    avatar_url?: string;
    is_verified: boolean;
    auth_provider: 'email' | 'google' | 'github';
}

export interface TokenResponse {
    access_token: string;
    refresh_token: string;
    token_type: string;
}

export interface LoginRequest {
    email: string;
    password?: string;
}

export interface RegisterRequest {
    name: string;
    email: string;
    password?: string;
}
